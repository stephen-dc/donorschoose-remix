export function translate(text: string, language: MemeLanguage): string {
  switch (language) {
    case 'brainrot':
      return applyDict(text, brainrotDict)
    case 'simlish':
      return applyDict(text, simlishDict)
    case 'pirate':
      return applyDict(text, pirateDict)
  }
}
